namespace Tetra;

partial class DataContext
{
   protected sealed class Binding<T>
   {
      /* ------------------------------------------------------------ */
      // Factory Functions
      /* ------------------------------------------------------------ */

      public static Binding<T> Create(T initialValue)
         => new(initialValue);

      /* ------------------------------------------------------------ */
      // Properties
      /* ------------------------------------------------------------ */

      public T Pull()
         => _value;

      /* ------------------------------------------------------------ */
      // Methods
      /* ------------------------------------------------------------ */

      public void Push(T value)
         => _value = value;

      /* ------------------------------------------------------------ */
      // Private Fields
      /* ------------------------------------------------------------ */

      private T _value;

      /* ------------------------------------------------------------ */
      // Private Constructors
      /* ------------------------------------------------------------ */

      private Binding(T value)
         => _value = value;

      /* ------------------------------------------------------------ */
   }
}